package org.example.expression;

/**
 * Исключение, выбрасываемое при ошибках вычисления математических выражений.
 * <p>
 * Является непроверяемым ({@code RuntimeException}), так как ошибки обычно
 * связаны с некорректными данными на этапе выполнения (синтаксис, арифметические ограничения,
 * неизвестные идентификаторы) и требуют вмешательства пользователя или отладки.
 * <p>
 * <b>Типичные сценарии выбрасывания:</b>
 * <ul>
 *   <li>Синтаксические ошибки (непарные скобки, недопустимые символы)</li>
 *   <li>Арифметические ошибки (деление на ноль, логарифм от неположительного числа)</li>
 *   <li>Вызов незарегистрированной функции или переменной</li>
 *   <li>Некорректное количество аргументов у функции</li>
 *   <li>Ошибки ввода данных для переменных через {@code VariableProvider}</li>
 * </ul>
 *
 * @see Expression
 * @since 1.0
 */
public class ExpressionException extends RuntimeException {

    /**
     * Создаёт исключение с указанным сообщением об ошибке.
     *
     * @param message детальное описание причины ошибки
     */
    public ExpressionException(String message) {
        super(message);
    }
}